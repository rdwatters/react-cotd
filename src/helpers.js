export function getFunName() {
  const adjectives = ['hairy','fuzzy','friendly','pulchritudinous','intoxicated','tawdry','exhilarating','gauche','boring','enticing','invigorating','relaxing'];

  const nouns = ['women', 'men', 'children', 'teeth', 'feet', 'people', 'leaves', 'mice', 'geese', 'halves', 'knives', 'wives', 'lives', 'elves', 'loaves', 'potatoes', 'tomatoes', 'cacti', 'foci', 'fungi', 'nuclei', 'syllabuses', 'anal beads','poop','peepee','beer','marijuana','loneliness'];

  return `${rando(adjectives)}-${rando(adjectives)}-${rando(nouns)}`;
}
